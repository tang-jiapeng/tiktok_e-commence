package product

import (
	"context"

	"github.com/cloudwego/hertz/pkg/app"
	"github.com/cloudwego/hertz/pkg/protocol/consts"
	"tiktok_e-commerce/app/hertz/biz/service"
	"tiktok_e-commerce/app/hertz/biz/utils"
	common "tiktok_e-commerce/app/hertz/hertz_gen/hertz/common"
	product "tiktok_e-commerce/app/hertz/hertz_gen/hertz/product"
)

// GetProduct .
// @router product/:id [GET]
func GetProduct(ctx context.Context, c *app.RequestContext) {
	var err error
	var req product.GetProductReq
	err = c.BindAndValidate(&req)
	if err != nil {
		utils.SendErrResponse(ctx, c, consts.StatusOK, err)
		return
	}

	resp := &common.Empty{}
	resp, err = service.NewGetProductService(ctx, c).Run(&req)
	if err != nil {
		utils.SendErrResponse(ctx, c, consts.StatusOK, err)
		return
	}

	utils.SendSuccessResponse(ctx, c, consts.StatusOK, resp)
}

